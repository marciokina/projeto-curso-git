#inserir dados em fahrenheit
temperaturaFahrenheit = input("Digte uma temperatura em fahrenheit: ")

#conversão dos dados em fahrenheit para celsius
temperaturaCelsius = (float(temperaturaFahrenheit)-32)*5/9

#apresentação dos dados em celsius
print("A temperatura em celsius é", temperaturaCelsius)
